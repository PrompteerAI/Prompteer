// Generates a web locale manifest from <app>/src/messages/*.json so adding a
// locale is a message-file operation instead of an app-code edit.
import { readdir, readFile, writeFile } from "node:fs/promises";
import { basename, dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = dirname(
  fileURLToPath(new URL("../package.json", import.meta.url)),
);
const appDir = resolve(repoRoot, process.argv[2] ?? "apps/web");
const messagesDir = join(appDir, "src/messages");
const outputFile = join(appDir, "src/i18n/locales.generated.ts");
const defaultLocale = "en";
const localePattern = /^[a-z]{2,3}(?:-[A-Za-z0-9]{2,8})*$/;

const messageFiles = (await readdir(messagesDir))
  .filter((fileName) => fileName.endsWith(".json"))
  .sort((left, right) => left.localeCompare(right));

const locales = messageFiles.map((fileName) => basename(fileName, ".json"));
validateLocales(locales);
await validateMessageShapes(messageFiles);
await writeLocalesManifest(locales);

console.log(
  `Generated ${locales.length} locale(s) for ${relativeAppDir()}: ${locales.join(", ")}`,
);

function validateLocales(locales) {
  if (!locales.includes(defaultLocale)) {
    throw new Error(
      `Missing required default locale file: ${defaultLocale}.json`,
    );
  }
  for (const locale of locales) {
    if (!localePattern.test(locale)) {
      throw new Error(
        `Invalid locale file name '${locale}.json'. Use a BCP 47-style tag like en or en-US.`,
      );
    }
  }
}

async function validateMessageShapes(messageFiles) {
  const defaultKeys = flattenKeys(
    JSON.parse(
      await readFile(join(messagesDir, `${defaultLocale}.json`), "utf8"),
    ),
  );

  for (const fileName of messageFiles) {
    const locale = basename(fileName, ".json");
    const messages = JSON.parse(
      await readFile(join(messagesDir, fileName), "utf8"),
    );
    const keys = flattenKeys(messages);
    const missingKeys = [...defaultKeys].filter((key) => !keys.has(key));
    if (missingKeys.length > 0) {
      throw new Error(
        `${locale}.json is missing ${missingKeys.length} key(s): ${missingKeys
          .slice(0, 10)
          .join(", ")}`,
      );
    }
  }
}

function flattenKeys(value, prefix = "") {
  if (!isRecord(value)) {
    throw new Error(
      `Expected message namespace '${prefix || "<root>"}' to be an object.`,
    );
  }

  const keys = new Set();
  for (const [key, child] of Object.entries(value)) {
    const path = prefix ? `${prefix}.${key}` : key;
    if (isRecord(child)) {
      for (const nestedKey of flattenKeys(child, path)) {
        keys.add(nestedKey);
      }
    } else {
      keys.add(path);
    }
  }
  return keys;
}

function isRecord(value) {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

async function writeLocalesManifest(locales) {
  const orderedLocales = [
    defaultLocale,
    ...locales.filter((locale) => locale !== defaultLocale),
  ];
  const serializedLocales = orderedLocales
    .map((locale) => `"${locale}"`)
    .join(", ");
  const contents = `// Generated by scripts/generate-web-locales.mjs from src/messages/*.json.
// Do not edit by hand; add or remove locale JSON files instead.
export const locales = [${serializedLocales}] as const;
export type AppLocale = (typeof locales)[number];
export const defaultLocale: AppLocale = "${defaultLocale}";
`;
  await writeFile(outputFile, contents);
}

function relativeAppDir() {
  return appDir.startsWith(repoRoot)
    ? appDir.slice(repoRoot.length + 1)
    : appDir;
}
